void EPD_WINDOW (uint8_t x1, uint8_t x2, uint8_t y1, uint8_t y2, uint8_t* schwarz, uint8_t* gelb){
	uint16_t pixel = (((x2 - x1)+1) * (y2 - y1));

	TRANSC(0x91);
		
	TRANSC(0x90);
	TRANSD(x1<<3);
	TRANSD((x2<<3) + 5);
	TRANSD(y1>>8);          //DEFINE WINDOW
	TRANSD(y1);
	TRANSD(y2>>8);
	TRANSD(y2);
	TRANSD(0x01);
	

	
	PIC_DISPLAY(schwarz, gelb, pixel);
	
	EPWAIT();
	TRANSC(0x92);
}

